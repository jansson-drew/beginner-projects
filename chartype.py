#Character Type Lab

char1 = input("Enter a character: ")
if(char1.islower() == True):
    print(char1, "is a lower case letter.")
elif(char1.isdigit() == True):
    print(char1, "is a digit.")
elif(char1.isalnum() == False):
    print(char1, "is a non-alphanumeric character.")
elif(char1.isupper() == True):
    print(char1, "is an upper case letter.")

